// Shared frontend build-and-publish helper (FR-012): the ONE code path that ships the frontend
// to Vercel, used by BOTH the manual `npm run deploy` (tools/scripts/deploy.mjs) and the CI
// `deploy` job (.github/workflows/ci.yml). Builds `apps/frontend` against the deployed
// backend's CDK outputs and publishes the prebuilt bundle with `vercel deploy --prebuilt`,
// so Vercel never rebuilds and the bundle always carries the just-deployed backend's values.

import { spawn } from 'node:child_process';
import { cpSync, existsSync, mkdirSync, renameSync, rmSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..', '..');
const FRONTEND_DIR = path.join(ROOT, 'apps/frontend');
const DIST_DIR = path.join(FRONTEND_DIR, 'dist');
const VERCEL_OUTPUT_DIR = path.join(FRONTEND_DIR, '.vercel/output');

function log(msg) {
  console.log(`\x1b[35m[vercel-deploy]\x1b[0m ${msg}`);
}

function run(cmd, args, opts = {}) {
  return new Promise((resolve, reject) => {
    const child = spawn(cmd, args, { stdio: 'inherit', ...opts });
    child.on('error', reject);
    child.on('exit', (code) =>
      code === 0 ? resolve() : reject(new Error(`${cmd} ${args.join(' ')} exited with ${code}`)),
    );
  });
}

/** Like `run`, but captures stdout (the Vercel CLI prints the deployment URL there). */
function runCapture(cmd, args, opts = {}) {
  return new Promise((resolve, reject) => {
    const child = spawn(cmd, args, { stdio: ['inherit', 'pipe', 'inherit'], ...opts });
    let out = '';
    child.stdout.on('data', (chunk) => {
      out += chunk;
      process.stdout.write(chunk);
    });
    child.on('error', reject);
    child.on('exit', (code) =>
      code === 0
        ? resolve(out.trim())
        : reject(new Error(`${cmd} ${args.join(' ')} exited with ${code}`)),
    );
  });
}

/**
 * Pure mapping of CDK stack outputs + release version → the production `VITE_*` build env.
 * Only public values — every key is `VITE_`-prefixed and safe to bundle (FR-004, SC-008).
 * `VITE_COGNITO_ENDPOINT` is forced empty so a production build always uses real Cognito
 * SRP, never the local emulator/proxy.
 */
export function buildProdEnv(outputs, version) {
  return {
    VITE_API_BASE_URL: String(outputs.apiBaseUrl ?? '').replace(/\/$/, ''),
    VITE_COGNITO_USER_POOL_ID: outputs.userPoolId,
    VITE_COGNITO_CLIENT_ID: outputs.clientId,
    VITE_COGNITO_ENDPOINT: '',
    VITE_APP_VERSION: version || '0.0.0-dev',
  };
}

/**
 * Build the frontend for production and publish it to Vercel.
 *
 * @param {object} opts
 * @param {string} opts.apiBaseUrl  CDK output `ApiBaseUrl`
 * @param {string} opts.userPoolId  CDK output `UserPoolId`
 * @param {string} opts.clientId    CDK output `UserPoolClientId`
 * @param {string} [opts.version]   release SemVer for `VITE_APP_VERSION` (default `0.0.0-dev`)
 * @param {string} [opts.token]     Vercel token; omitted → the CLI's logged-in session
 * @param {boolean} [opts.prod]     `true` → production deploy; else a preview deploy
 * @returns {Promise<string>} the deployment URL
 */
export async function deployFrontendToVercel({ apiBaseUrl, userPoolId, clientId, version, token, prod }) {
  if (!apiBaseUrl || !userPoolId || !clientId) {
    throw new Error('deployFrontendToVercel: apiBaseUrl, userPoolId and clientId are required');
  }

  // Vite loads .env.local in every mode and it OVERRIDES .env.production — move it aside so the
  // production build can't inherit the local /api + /cognito values (FR-004, SC-008).
  const moved = [];
  for (const name of ['.env.local', '.env.production.local']) {
    const p = path.join(FRONTEND_DIR, name);
    if (existsSync(p)) {
      const bak = `${p}.deploy-bak`;
      renameSync(p, bak);
      moved.push([bak, p]);
    }
  }
  const prodEnvFile = path.join(FRONTEND_DIR, '.env.production');
  const env = buildProdEnv({ apiBaseUrl, userPoolId, clientId }, version);
  writeFileSync(
    prodEnvFile,
    [
      '# Generated by the deploy pipeline — production build values from CDK outputs.',
      ...Object.entries(env).map(([k, v]) => `${k}=${v}`),
      '',
    ].join('\n'),
  );

  try {
    log(`building frontend (version ${env.VITE_APP_VERSION})…`);
    // --skip-nx-cache is essential: Nx doesn't track .env files as inputs, so without it a
    // cached bundle (with stale/local values) would be reused instead of rebuilt.
    await run('npx', ['nx', 'build', 'frontend', '--skip-nx-cache'], { cwd: ROOT });

    // `vercel deploy --prebuilt` consumes the Build Output API directory, so assemble it from
    // the Nx-built dist: static assets + the SPA fallback route (same rewrite as vercel.json,
    // which governs non-prebuilt/dashboard deploys).
    rmSync(VERCEL_OUTPUT_DIR, { recursive: true, force: true });
    mkdirSync(VERCEL_OUTPUT_DIR, { recursive: true });
    writeFileSync(
      path.join(VERCEL_OUTPUT_DIR, 'config.json'),
      JSON.stringify(
        { version: 3, routes: [{ handle: 'filesystem' }, { src: '/(.*)', dest: '/index.html' }] },
        null,
        2,
      ),
    );
    cpSync(DIST_DIR, path.join(VERCEL_OUTPUT_DIR, 'static'), { recursive: true });

    log(`publishing to Vercel (${prod ? 'production' : 'preview'})…`);
    const args = ['vercel', 'deploy', '--prebuilt', '--yes'];
    if (prod) args.push('--prod');
    if (token) args.push(`--token=${token}`);
    const url = await runCapture('npx', args, { cwd: FRONTEND_DIR });
    log(`deployed → ${url}`);
    return url;
  } finally {
    rmSync(prodEnvFile, { force: true });
    for (const [bak, orig] of moved) renameSync(bak, orig);
  }
}

// CLI entry (used by the CI deploy job): values arrive via environment variables.
const isMain =
  process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url);
if (isMain) {
  deployFrontendToVercel({
    apiBaseUrl: process.env.DEPLOY_API_BASE_URL,
    userPoolId: process.env.DEPLOY_USER_POOL_ID,
    clientId: process.env.DEPLOY_USER_POOL_CLIENT_ID,
    version: process.env.DEPLOY_APP_VERSION,
    token: process.env.VERCEL_TOKEN,
    prod: process.env.DEPLOY_PROD === '1',
  })
    .then(async (url) => {
      // Expose the deployment URL to later workflow steps (e.g. the PR preview comment).
      if (process.env.GITHUB_OUTPUT) {
        const { appendFileSync } = await import('node:fs');
        appendFileSync(process.env.GITHUB_OUTPUT, `url=${url}\n`);
      }
    })
    .catch((err) => {
      console.error('\nVercel deploy failed:', err.message ?? err);
      process.exit(1);
    });
}
